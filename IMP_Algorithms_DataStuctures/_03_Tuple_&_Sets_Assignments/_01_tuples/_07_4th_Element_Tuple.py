'''
get the 4th element and 4th element from last of a tuple`
'''
my_tuple = ('red', 'yellow', 'green', 'Indigo', 'orange')

# Get 4th element of tuple
print('4th element in tuple - ',my_tuple[3])
# 4th Element from last
print(f'4th element from last - ', my_tuple[-4])
# Last element of the tuple
print('Last Element - {}'.format(my_tuple[-1]))